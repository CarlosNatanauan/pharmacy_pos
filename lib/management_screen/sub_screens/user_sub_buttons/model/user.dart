class User {
   String firstName;
   String middleName;
   String lastName;
   String username;
   String password;
   String imageUrl;
   String role;

  User({
    required this.firstName,
    required this.middleName,
    required this.lastName,
    required this.username,
    required this.password,
    required this.imageUrl,
    required this.role,
  });
}
